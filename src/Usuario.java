public class Usuario {
    private String dni;
    private String name;
    private String correo;

    public Usuario(String dni, String name, String correo) {
        this.dni = dni;
        this.name = name;
        this.correo = correo;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }
}
